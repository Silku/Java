package chapter1;

public class VariableEx2 {

	public static void main(String[] args) {
		
		char c1 = '안';
		char c2 = '녕';
		char c3 = '하';
		char c4 = '세';
		char c5 = '요';
	
		System.out.println(""+c1+c2+c3+c4+c5+".");
		
		/*변수
		name 1~3 만들고 본인의 이름을 입력합니다.
		*/
		char name1 = '김';
		char name2 = '태';
		char name3 = '우';
	
		int year = 1919;
		int month = 12;
		int day = 34;
		
		
	
		System.out.println(""+name1+name2+name3+ "의 생년월일은"  + year + "." + month + "." + day +"입니다.");
	}

}
